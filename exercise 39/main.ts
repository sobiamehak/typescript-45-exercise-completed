function describe_function(city:string,country:string):string{
return`${city} , ${country}`;
}
//calling a function in print a return value
describe_function("karachi" , "pakistan");
console.log(describe_function("karachi" , "pakistan"))
describe_function("tokyo","Japan");
console.log(describe_function("tokyo","Japan"))
describe_function("Barlin","germany")
console.log(describe_function("Barlin","germany"))
